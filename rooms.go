package tcmrsv

import (
	"slices"
	"strings"
)

type GetRoomsFilteredParams struct {
	Name         *string
	ID           *string
	PianoNumbers []int
	PianoTypes   []RoomPianoType
	Floors       []int
	IsBasement   *bool
	Campuses     []Campus
}

func GetRoomsFiltered(params GetRoomsFilteredParams) []Room {
	var result []Room
	for _, room := range GetRooms() {
		if params.Name != nil && !strings.Contains(strings.ToLower(room.Name), strings.ToLower(*params.Name)) {
			continue
		}
		if params.ID != nil && *params.ID != room.ID {
			continue
		}
		if len(params.PianoNumbers) > 0 {
			matched := slices.Contains(params.PianoNumbers, room.PianoNumber)
			if !matched {
				continue
			}
		}
		if len(params.PianoTypes) > 0 {
			matched := slices.Contains(params.PianoTypes, room.PianoType)
			if !matched {
				continue
			}
		}
		if len(params.Floors) > 0 {
			matched := slices.Contains(params.Floors, room.Floor)
			if !matched {
				continue
			}
		}
		if params.IsBasement != nil && room.IsBasement != *params.IsBasement {
			continue
		}
		if len(params.Campuses) > 0 {
			matched := slices.Contains(params.Campuses, room.Campus)
			if !matched {
				continue
			}
		}
		result = append(result, room)
	}
	return result
}

